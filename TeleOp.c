void initializeRobot() {
	return;
}

void stopRobot() {
	motor[motorA] = 0;
	motor[motorB] = 0;
	motor[motorC] = 0;
	motor[motorD] = 0;
}

void moveForward() {
	motor[motorA] = 50;
	motor[motorD] = 50;
}

void moveBackward() {
	motor[motorA] = -50;
	motor[motorD] = -50;
}

void moveSideways() {
	motor[motorB] = 50;
	motor[motorC] = 50;
}

void moveSidewaysReverse() {
	motor[motorB] = -50;
	motor[motorC] = -50;
}

void turnLeft() {
	motor[motorB] = 50;
	motor[motorD] = 50;
	motor[motorC] = -50;
	motor[motorA] = -50;
}

void turnRight() {
	motor[motorB] = -50;
	motor[motorD] = -50;
	motor[motorC] = 50;
	motor[motorA] = 50;
}

void raiseArm() {
	motor[motorE] = 25;
	motor[motorF] = 25;
}

task main() {
	initializeRobot();

	waitForStart();		// wait for start of tele-op phase

	while (true) {

		getJoystickSettings(joystick); // Fetches the data from the joystick


		nxtDisplayTextLine(2, "Top hat: %d", joystick.joy1_TopHat);

		/*
		THE FOLLOWING CODE CONTROLS THE MOVEMENT OF THE WHEELS
		NOT INCLUDING THE TURNS
		Checks the value of the top_hat
		Range of 0-1: Up
		2 is Right (do more testing to check for violatle)
		Range of 3-4: Down
		Range of 5-6: Left
		Range of 7: Up

		Mapping to wheel motion
		Up : moveForward()
		Down: moveBackward()
		Left: moveSidewaysReverse()
		Right: moveSideways()
		*/

		if (joystick.joy1_TopHat == 0 || joystick.joy1_TopHat == 1 || joystick.joy1_TopHat == 7) {
			moveForward();
			} else if (joystick.joy1_TopHat == 2) {
			moveSideways();
			} else if (joystick.joy1_TopHat == 3 || joystick.joy1_TopHat == 4) {
			moveBackward();
			} else if (joystick.joy1_TopHat == 5 || joystick.joy1_TopHat == 6) {
			moveSidewaysReverse();
		} else {}

		// THE FOLLOWING CODE CONTROLS THE TURNING OF THE ROBOT

		if (joy1Btn(1)) {
			turnLeft();
			} else if (joy1Btn(3)) {
			turnRight();
		}

		if (joystick.joy2_TopHat == 0 || joystick.joy2_TopHat == 1 || joystick.joy2_TopHat == 7) {
			raiseArm();
			} else if (joystick.joy2_TopHat == 3 || joystick.joy2_TopHat == 4) {
			lowerArm();
		} else {}
	}
}
